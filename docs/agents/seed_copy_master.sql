Exemplos:

* Enquanto muitos procuram atalhos, poucos constroem fundamentos.
* Enquanto os maus estão cheios de certezas, os bons estão cheios de dúvidas.

Modelo:

"Quanto mais ______, menos ______."

Exemplos:

* Quanto mais você tenta agradar todos, menos alguém lembra de você.
* Quanto mais você corre atrás da atenção, menos autoridade transmite.

